public class HW5 {
    //Да се изведат числата от 1 до 100 във възходящ и в низходящ ред и да се отпечатат получените резултати с помоща на for цикъл
    public static void main(String[] args) {

        for (int i=1;i<101;i++)
        {
            System.out.println(i);
        }
        for (int i=100;i>0;i--)
        {
            System.out.println(i);
        }
    }
}
